function scuberGreetingForFeet(number) {

  let result
  if (number <= 400) {
    result = "This one is on me!";
  }
  else if (number > 2000) {
    result = "I will gladly take your thirty bucks."
  }
  else if (number >= 2500) {
    result = "No can do."
  }
  return result
}
